export class Calculator {
    Add(x: number, y: number): number {
        return x + y;
    }

    Substract(x: number, y: number): number {
        return x - y;
    }

    Multiply(x: number, y: number): number {
        return x * y;
    }

    Divide(x: number, y: number): number {
        return x / y;
    }
}
